#  A for loop is used to iterate through a sequence like list, tuple, or string[iterables]

# for i in range(4):
#   print(i)


# range(start, stop, step_size) 
for i in range(0, 100, 4):
  print(i)


# For loop with lists
l = [1, 4, 6, 56, 23, 89]
for i in l:
  print(i)


# For loop with tuple
t = (4, 84, 45, 13)
for i in t:
  print(i)


#  For loop with string
s = "Hello"
for i in s:
  print(i)